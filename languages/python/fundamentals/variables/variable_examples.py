

# Python Variables Examples

# Variable Declaration
application_name = "Backend Engineering"
version = 1
cpu_usage = 78.5
is_active = True

print("Application:", application_name)
print("Version:", version)
print("CPU Usage:", cpu_usage)
print("Active:", is_active)

# Dynamic Typing
value = 100
print("Integer:", value)

value = "Python"
print("String:", value)

value = False
print("Boolean:", value)

# Multiple Variable Assignment
cpu, memory, disk = 70, 80, 60

print(cpu)
print(memory)
print(disk)

# Same Value Assignment
x = y = z = 0

print(x, y, z)

# Type Checking
service_name = "Auth-Service"
request_count = 150

print(type(service_name))
print(type(request_count))

# Backend Engineering Example
database_host = "localhost"
database_port = 5432
feature_enabled = True
supported_services = ["api", "database", "cache"]

print(database_host)
print(database_port)
print(feature_enabled)
print(supported_services)